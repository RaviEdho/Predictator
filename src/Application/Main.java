    private void btnPredictActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPredictActionPerformed
        CardLayout menu = (CardLayout) pnlBody.getLayout();
        int pilihan = 0;
        int daerah = 0;
        if (swtchProdi1.isSelected()) {
            pilihan = 0;
        }
        else if (swtchProdi2.isSelected()) {
            pilihan = 1;
        }
        else if (swtchProdi3.isSelected()) {
            pilihan = 2;
        }
        else if (swtchProdi4.isSelected()) {
            pilihan = 3;
        }
        else if (swtchProdi5.isSelected()) {
            pilihan = 4;
        }
        else if (swtchProdi6.isSelected()) {
            pilihan = 5;
        }
        else if (swtchProdi7.isSelected()) {
            pilihan = 6;
        }
        
        if (swtchDaerahJakarta.isSelected()) {
            daerah = 1;
        }
        else {
            daerah = 0;
        }
        Data.Predict(daerah);
        Data.TampilPeluang(pilihan);
        menu.show(pnlBody, "hasilprediksi");
    }//GEN-LAST:event_btnPredictActionPerformed

